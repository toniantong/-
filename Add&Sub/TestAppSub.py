# 為了示範，我們定義一個純文字的邏輯字典
# 格式: ('字元甲', '字元乙') : ('進位字元', '當前字元')
octal_logic = {
    ('7', '2'): ('1', '1'),  # 7+2 在 8進位是 11，所以進位 1，留下 1
    ('1', '0'): ('0', '1'),  # 1+0 = 1，不進位 (0)，留下 1
    ('1', '1'): ('0', '2'),  # 1+1 = 2，不進位 (0)，留下 2
    ('0', '2'): ('0', '2'),
    # ... (在完整程式中，這裡會列出 '0' 到 '7' 的所有 64 種組合)
}
